                <h4>More past tenses</h4>
                <ol>
                    <li>Was he at home yesterday? - I think so, but I didn't phone.</li>
                    <li>He called yesterday, but he didn't see me. - Did he phone? - I don't think so.</li>
                    <li>These are the interrogative and negative forms in the past.</li>
                    <li>You do not change the verb, you simply put the auxiliary "do" into the past "did":</li>
                    <li>I did not (didn't) like the food. He did not (didn't) phone. We did not (didn't) like him.</li>
                    <li>Questions are easy, too: Did he like it? Did she phone?</li>
                    <br />
                    <li>Did you like the play last night? - I didn't see it.</li>
                    <li>But did you go to the theatre? - Yes, but I was so tired I closed my eyes.</li>
                    <li>Tell me, was the play interesting? - I didn't understand very much, it was in Greek.</li>
                    <li>Then why did you go? - I liked the main actor.</li>
                    <li>Did he act well? - I didn't watch the acting. I looked at him.</li>
                    <li>What did you do after the play? - I dreamed about the actor!</li>
                    <li>He did not, He didn't; he didn't like the play.</li>
                    <li>Did they... ?; did they phone you?</li>
                </ol>

                <h4>Больше о прошедшем времени</h4>
                <ol>
                    <li>Он был вчера дома? - Думаю, что да, но я не звонил.</li>
                    <li>Он зашёл вчера, но меня не видел, - Он звонил? Не думаю.</li>
                    <li>Это вопросительные и отрицательные формы в прошедшем времени.</li>
                    <li>Вы не меняете основной глагол, а меняете только вспомогательный глагол "do" на форму прошедшего времени [прошедшую форму] "did"</li>
                    <li>Мне не понравилась эта еда. Он не позвонил. Мы его не полюбили (Он нам не понравился).</li>
                    <li>Вопросы тоже лёгкие: Он полюбил это? (Ему это понравилось) Она позвонила?</li>
                    <br />
                    <li>Тебе понравилась пьеса вчера вечером? - Я её не видела.</li>
                    <li>Но ты пошла в театр? - Да, но я не была такая усталая, что закрыла глаза.</li>
                    <li>Скажи мне, была ли пьеса интересна? - Очень многого я не поняла, она исполнялась по-гречески.</li>
                    <li>Тогда почему ты пошла, - Мне понравился актёр, играющий главную роль.</li>
                    <li>Он хорошо играл? - Я не смотрела на игру. Я смотрела на него.</li>
                    <li>Что ты делала после [просмотра] пьесы? - Мечтала об актёре.</li>
                    <li>Он не, он не (сокращённая форма). Ему не понравилась пьеса.</li>
                    <li>Они...? Они звонили тебе?</li>
                </ol>
            </div>

            <h3>Fifty-second (52nd) Lesson</h3>
            <div>
                <ol>
                    <li>Here are a few more examples of regular verbs in the present, in the past and with the past participle:</li>
                    <li>I hope, I hoped, I have hoped. He lives, he lived, he has lived. We finish, we finished, we have finished.</li>
                    <li>They talk, they talked, they have talked. She changes, she changed, she has changed. You play, you played, you have played.</li>
                    <br />
                    <li>Let's practise the past of "do":</li>
                    <li>Does he smoke? Did he smoke? We don't ask questions; we didn't ask questions.</li>
                    <li>What do you do? What did you do?</li>
                    <li>Does he wait? Did he wait? She doesn't answer. She didn't answer.</li>
                    <li>"Can" and "will" are irregular and become "could" and "would" in the past. They have no past participle.</li>
                    <li>I can begin now. I couldn't begin yesterday.</li>
                    <li>He wouldn't work this morning, but he will now.</li>
                    <li>I have (I've), I had.</li>
                    <li>I am (I'm), we are (we're), I was, we were, I have been.</li>
                </ol>

                <ol>
                    <li>Вот немного больше примеров правильных глаголов в настоящем и прошедшем времени, а также в форме действительного причастия.</li>
                    <li>Я надеюсь, я надеялся, я надеялся. Он живёт, он жил, он жил. Мы заканчиваем, мы закончили, мы закончили.</li>
                    <li>Они разговаривают, они разговаривали, они разговаривали. Она меняет, она поменяла, она поменяла. Ты играешь, ты играл, ты играл.</li>
                    <br />
                    <li>Отработаем на практике прошедшее время глагола do.</li>
                    <li>Он курит? Он курил? Мы не задаём никаких вопросов. Мы не задавали никаких вопросов.</li>
                    <li>Что ты делаешь? Что ты делал?</li>
                    <li>(Разве) он ждёт? Ждал ли он? Она не отвечает. Она не отвечала.</li>
                    <li>Can - мочь, уметь и will являются неправильными глаголами с формами could и would в прошедшем времени. Эти глаголы не образуют </li>
                    <li>твительного причастия.</li>
                    <li>Я могу начать сейчас. Я не могла начать вчера.</li>
                    <li>Он не хотел работать [этим] утром, но будет (работать) сейчас.</li>
                    <li>У меня есть / я имею, у меня была / я имела.</li>
                    <li>Я [есть], мы [есть], я была, мы были, я была.</li>
                </ol>
            </div>

