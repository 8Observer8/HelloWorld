            <h3>Forty-third (43th) Lesson</h3>
            <div>
                <h4>The future</h4>
                <ol>
                    <li>We will now look at the future tense.</li>
                    <li>You form the future tense by putting "will" in front of nearly all our verbs.</li>
                    <li>For instance, the verb "to dress", in the future, becomes "I will dress; you will dress".</li>
                    <li>We continue with the contraction: "We'll dress, you'll dress, they'll dress."</li>
                    <li>You see, it's easy!</li>
                    <li>Let's look at some sentences in the future:</li>
                    <li>I'll go to the cinema tonight if you'll come with me.</li>
                    <li>You will learn English quickly if you read a lesson every day.</li>
                    <li>The word "shall" is mainly used for suggestions: It's raining, shall we take a taxi?</li>
                    <li>How much money will you need? Shall I give you some more?</li>
                    <li>Will you give me some more coffee, please?</li>
                    <li>How will you go to work? The tube's on strike. - I'll take my car.</li>
                    <li>Shall I phone the office and tell them you'll be late?</li>
                    <li>I'll drive; you'll be late; you will have problems.</li>
                </ol>

                <h4>Будущее время</h4>
                <ol>
                    <li>Давайте посмотрим теперь на будущее время.</li>
                    <li>Будущее время образуем, помещая "will" перед почти всеми [нашими] глаголами.</li>
                    <li>Например, глагол "to dress" (одеваться) в будущем времени имеет форму [становится] "I will dress; you will dress".</li>
                    <li>Продолжаем сокращения [с сокращениями]: "We'll dress, you'll dress, they'll dress."</li>
                    <li>Видите, это легко!</li>
                    <li>Давайте присмотримся теперь к нескольким предложениям в будущем времени.</li>
                    <li>Я пойду в кино сегодня вечером, если ты пойдёшь со мной.</li>
                    <li>Ты быстро выучишь английский, если будешь читать один урок каждый день.</li>
                    <li>Слово shall употребляется главным образом с целью предложения чего-либо: Идёт дождь, поедем на такси?</li>
                    <li>How much money will you need? Тебе дать немного больше [денег]?</li>
                    <li>Дашь мне чуть [немного] больше кофе, пожалуйста?</li>
                    <li>Как ты поедешь на работу? Метро бастует. - Поеду на своей машине машине [возьму свою машину].</li>
                    <li>Позвонить в офис и сказать им, что ты опоздаешь?</li>
                    <li>Я поеду на машине; ты опоздаешь; у тебя будут проблемы.</li>
