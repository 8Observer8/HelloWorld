                    <li>Obviously, you do not know by heart every word and every expression we have seen: that would be too perfect.</li>
                    <li>Learning a foreign language is a matter of patience, regular daily repetition - and optimism.</li>
                    <li>Somebody once said that English was like Mount Everest:</li>
                    <li>because access is easy, but the summit is impossible to reach.</li>
                    <li>We think this is wrong because nobody speaks his own language perfectly.</li>
                    <li>You must try, by regular practice, to climb as high as you want, until you feel comfortable.</li>
                    <li>But be careful. In order not to fall, you must practice as often as possible.</li>
                    <li>You will learn new words and expressions and forget them, and learn them again and forget them once more.</li>
                    <li>But you are making progress. Compare what you know now with what you knew three months ago.</li>
                    <li>The hardest and most tedious part of your work is done; before long, you will speak fluently.</li>
                    <li>But remember: in order to stay on the mountain, you need daily practice.</li>
                </ol>

                <ol>
                    <li>Это наш сотый урок. Если Вы уделили, включая повторения, в среднем около получаса</li>
                    <li>каждому из предыдущих уроков, то в сумме [это даёт] (занимались) почти пятьдесят часов.</li>
                    <li>Довольны ли Вы результатом своей работы?</li>
                    <li>Конечно же, Вы не знаете на память каждое слово и каждое выражение, которые нам встречались: это было бы верхом совершенства [слишком совершенно].</li>
                    <li>Изучение иностранного языка - это вопрос терпения, регулярного ежедневного повторения - и оптимизма.</li>
                    <li>Кто-то когда-то сказал, что английский язык как гора Эверест:</li>
                    <li>поскольку подход лёгок, а вершину покорить невозможно.</li>
                    <li>Мы считаем, что это не так, потому что никто не владеет [говорит] своим собственным языков в совершенстве.</li>
                    <li>Вы должны пробовать - посредством регулярных упражнений - взобраться так высоко, как хотите, пока не почувствуете себя хорошо.</li>
                    <li>Но будьте осторожны! Для того, чтобы не упасть, Вы должны упражняться так часто, как это (только) возможно.</li>
                    <li>Вы будете учить новые слова и выражения и забывать их, учить их снова и забывать ещё раз.</li>
                    <li>А Вы (ведь) делаете успехи.Сравните то, что Вы знаете сейчас, с тем, что знали три месяца назад.</li>
                    <li>Самая тяжёлая и скучная часть Вашей работы уже сделана, скоро Вы будете говорить бегло.</li>
                    <li>Но помните: для того, чтобы удержаться на высоте [остаться на горе], Вы нуждаетесь в ежедневных занятиях.</li>
                </ol>
            </div>

