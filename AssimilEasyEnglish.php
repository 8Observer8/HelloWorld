                    <li>No, but would you take a message for me, please?</li>
                    <li>I'd be delighted.</li>
                    <br />
                    <li>"Would" is the conditional in English and is placed in front of the infinitive.</li>
                    <li>The contraction is easy: "Would" becomes ... 'd.</li>
                    <li>For instance: I'd like a cup of tea.</li>
                    <li>He'd help you. We'd prefer beer, if you have any.</li>
                    <li>Questions are easy, too: Would you like a cup of tea? Would you help me please?</li>
                    <li>Here are some more sentences: He would understand if you spoke more slowly.</li>
                    <li>He wouldn't ask for help if he didn't want it.</li>
                    <li>He wouldn't need a teacher if he spoke fluently.</li>
                    <br />
                    <li>A famous lawyer had lost a case and was very angry.</li>
                    <li>"If this is the law, he shouted, I'll burn my books!"</li>
                    <li>The judge replied: "It would be better if you read them".</li>
                </ol>

                <h4>Условные конструкции</h4>
                <ol>
                    <li>Что я могу для Вас сделать (чем могу Вам помочь), сэр? - Я хотел бы поговорить с мистером Дэвисом.</li>
