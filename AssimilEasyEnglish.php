            <h3>Fiftieth (50th) Lesson</h3>
            <div>
                <h4>The past tense</h4>
                <ol>
                    <li>We worked hard yesterday, and today we must look at something new.</li>
                    <li>You looked at the lesson and listened to the records (or tapes), so now you are ready to learn the past tense.</li>
                    <li>It is very simple. You add "ed" to the infinitive if it ends in a consonant;</li>
                    <li>and simply "-d" to the infinitive ending in "e".</li>
                    <li>For example: to look, I looked; to work, he worked;</li>
                    <li>To like, they liked; to smile, we smiled.</li>
                    <li>There are, of course, some irregular verbs, but they are not too complicated.</li>
                    <li>Lit's look at our old friends "to be" and "to have".</li>
                    <li>"To have" is extremely simple. It becomes "had": you had, he had.</li>
                    <li>"to be" has two forms: I was, he was; and; you were, they were.</li>
                    <li>You see how simple it is!</li>
                    <li>We had dinner at eight o'clock, John and Peter were there.</li>
                    <li>I had a cold last week and I was quite ill.</li>
                    <li>We hoped to see her but she was busy.</li>
                </ol>

                <h4>Прошедшее время</h4>
                <ol>
                    <li>Мы вчера хорошо [тяжело] поработали, а сегодня должны познакомиться с чем-то новым [посмотреть на что-то новое].</li>
                    <li>Вы видели материал урока и слышали компакт-дисковые либо магнитофонные записи, и сейчас (уже) готовы (к тому, чтобы) научиться </li>
                    <li>едшему времени.</li>
                    <li>Это очень просто. Вы прибавляете "ed" к инфинитиву, если он заканчивается на согласную букву;</li>
                    <li>и просто "d" к инфинитиву, заканчивающемуся на гласную букву.</li>
                    <li>Например: to look, I looked; to work, he worked;</li>
                    <li>To like, they liked; to smile, we smiled.</li>
                    <li>Конечно, же существует небольшое количество неправильных глаголов, но и они не слишком сложны.</li>
                    <li>Посмотрим на наших старых знакомых "быть" и "иметь".</li>
                    <li>Глагол "иметь" - чрезвычайно прост. (В прошедшем времени) он используется в форме в форме had - имел/а, ты имел/вы имели, он имел.</li>
                    <li>"Быть" имеет две формы: I was, he was; and; you were, they were.</li>
                    <li>Вы видите, как это просто!</li>
                    <li>Мы ели ужин в семь часов, Джон и Питер там были.</li>
                    <li>Я простудился на прошлой неделе и сильно заболел [был совершенно больным].</li>
                    <li>Мы надеялись её увидеть, но она была занята.</li>
                </ol>
            </div>

