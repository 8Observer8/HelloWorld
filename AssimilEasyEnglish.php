            <h3>Ninety-first (91st) Lesson</h3>
            <div>
                <h4>Revisions and Notes (повторение и объяснения)</h4>
                <ol>
                    <li>We have been talking - это новое время, с которым мы недавно познакомились. Оно называется Present Perfect Continuous Tense и составляет его конструкция: have/has + been + глагол с окончанием -ing. Оно выражает действие, которое началось в прошлом и только что закончилось, или ещё продолжается. В русском языке это время частично соответствует прошедшему, и частично настоящему времени. Have you been dancing? - Ты танцевал? She has been dancing since she was five. - Она танцует с тех пор, когда ей исполнилось пять [лет]. How long have you been reading it? Как долго ты это читал? Обстоятельства времени, которые чаще всего употребляются с этим временем - это: for - какое-то время, since - с какого-то времени. For three hours, for six years, for a long time; since last Saturday, since my birthday, since their childhood.</li>
                    <br />
                    <li>Would выполняет в английском языке много функций. Оно употребляется в выражении would like - я бы хотел/а. I'd like to go. - Я хотела бы пойти. Вопросы с употреблением would, так же как и с will служат для выражения вежливой просьбы: Would you get me a drink? - Не мог бы ты принести мне напиток? и когда мы хотим узнать, готов, ли кто-нибудь что-то сделать: Would you come? - Не пришёл бы ты? Заметьте, что такого типа конструкции не всегда переводятся на русский язык с помощью условного наклонения. Эти предложения можно передать по-русски с помощью вежливой формы повелительного наклонения, например, Would you get me a drink? - Будь добр, принеси мне, пожалуйста, напиток. или Ты можешь принести мне напиток?</li>
                    <br />
                    <li>Оборот would you mind выражает вежливое, но решительное приказание или запрет: Would you mind not smoking? - Будь добр, не кури. Would you mind coming? Будь добр, приди.</li>
                    <br />
                    <li>Мы узнали несколько выражений, таких как Oh dear! - О, Ой, О Боже! - которое употребляется тогда, когда мы удивлены, счастливы, разочарованы, напуганы и т.п. All right - хорошо, применяется в таком же значении, как О.K. Never mind - ничего страшного.</li>
                    <br />
                    <li>Not to - не. Try not to slam the door. - Постарайся не хлопать дверью. He told me not to move. - Он сказал мне, чтобы я не двигался.</li>
                    <br />
                    <li>Holiday - каникулы, отпуск. Holiday - один, два выходных. A holiday или holiday - более длинный отпуск. Но в выражении to be on holiday - быть в отпуске и three week's holiday - трёхнедельный отпуск, holiday встречается только в единственном числе.</li>
                    <br />
                    <li>The stairs - ступени, лестница. Upstairs - наверху (на втором этаже). Downstairs - внизу. She ran downstairs - она сбежала вниз (по лестнице, по ступеням). Staircase - лестничная клетка.</li>
                    <br />
                    <li>Просмотрите ещё раз уроки 88 и 89. Обратите внимание на стиль. В общении предложения более короткие, имеют больше прилагательных и восклицательных выражений. В письменном тексте предложения более продолжительные, а язык более формален.</li>
                </ol>
            </div>

