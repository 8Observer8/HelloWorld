                    <li>С удовольствием.</li>
                    <br />
                    <li>"Would" используется в условных конструкциях в английском языке и ставится перед инфинитивом.</li>
                    <li>Сокращённый вариант этой формы прост: "Would" трансформируется в ... 'd.</li>
                    <li>Например: Я хотел бы чашечку чая.</li>
                    <li>Он тебе поможет. Мы хотели бы пиво, если у тебя есть [какое-нибудь].</li>
                    <li>Вопросительные конструкции также просты: Ты хотел бы чашечку чая? Ты хотел бы мне помочь?</li>
